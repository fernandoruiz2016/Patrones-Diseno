    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try {
            if (txtNombre.getText().trim().isEmpty() || txtPrecio.getText().trim().isEmpty() || txtStock.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.");
                return;
            }

            if (cbRegion.getSelectedIndex() <= 0) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar una región válida.");
                return;
            }

            String nombre = txtNombre.getText().trim();
            double precio = Double.parseDouble(txtPrecio.getText().trim());
            int stock = Integer.parseInt(txtStock.getText().trim());

            String regionSeleccionada = cbRegion.getSelectedItem().toString();
            // Corregimos la comparación para que coincida con el ComboBox
            String regionKey = (regionSeleccionada.equals("España")) ? "España" : "Latam";

            controller.registrarProducto(regionKey, nombre, precio, stock);

            JOptionPane.showMessageDialog(this, "Producto registrado en " + regionSeleccionada);
            limpiarCamposRegistro();

            if (regionKey.equals("España")) {
                radEs.setSelected(true);
            } else {
                radLat.setSelected(true);
            }

            btnBuscarActionPerformed(null);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: Precio y Stock deben ser numéricos.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error inesperado: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void limpiarCamposRegistro() {
        txtNombre.setText("");
        txtPrecio.setText("");
        txtStock.setText("");
        cbRegion.setSelectedIndex(0);
    }

