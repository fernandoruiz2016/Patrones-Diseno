    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String region = "";
        if (radEs.isSelected()) {
            region = "España";
        } else if (radLat.isSelected()) {
            region = "Latam";
        } else if (radCon.isSelected()) {
            region = "Consolidado";
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una región para ver el inventario.");
            return;
        }

        // 1. Obtener el modelo de la tabla actual
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tbInv.getModel();

        // 2. Limpiar la tabla antes de mostrar nuevos datos
        modelo.setRowCount(0);

        // 3. Obtener los datos desde el controlador
        // Nota: Si es "Consolidado", el controlador debe unir las listas de ambas filiales
        Object[][] datos = controller.obtenerMatrizInventario(region);

        // 4. Llenar la tabla con los datos obtenidos
        for (Object[] fila : datos) {
            modelo.addRow(fila);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

